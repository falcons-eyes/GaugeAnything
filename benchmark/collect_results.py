"""GaugeBench v1.0 — 결과 수집·검증·리더보드 렌더러.

역할
----
1. experiments/results/ 의 canonical JSON에서 트랙별 공식 지표를 추출한다.
2. benchmark/expected_v1.json 의 핀 값과 허용오차 내 일치를 검증한다 (release gate).
3. benchmark/LEADERBOARD.md 를 렌더한다.

사용:
    python benchmark/collect_results.py            # 검증 + 리더보드 갱신
    python benchmark/collect_results.py --check    # 검증만 (CI/release gate)

드리프트가 나면 비정상 종료한다 — 숫자를 조용히 바꾸지 않는 것이 이 벤치마크의 계약이다.
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
RESULTS = ROOT / "experiments" / "results"
EXPECTED = Path(__file__).resolve().parent / "expected_v1.json"
LEADERBOARD = Path(__file__).resolve().parent / "LEADERBOARD.md"


def _load(name: str) -> dict:
    return json.loads((RESULTS / name).read_text(encoding="utf-8"))


def extract_metrics() -> dict[str, dict[str, float]]:
    """트랙별 공식 지표를 canonical JSON에서 추출."""
    out: dict[str, dict[str, float]] = {}

    gb = _load("gauge_bench.json")["results"]
    out["S_crackseg9k_segmentation"] = {
        "sam3_crack_miou": gb["sam3"]["crack_mIoU_mean"],
        "sam3_clean_rate": gb["sam3"]["noncrack_clean_rate"],
        "adaptive_crack_miou": gb["adaptive"]["crack_mIoU_mean"],
        "frangi_crack_miou": gb["frangi"]["crack_mIoU_mean"],
    }

    lt = _load("m2v2_logit_threshold.json")
    tab = _load("m2_specialist_tabular.json")
    conf = _load("m2_uncertainty_conformal.json")
    cc = conf["test"]["conformal_log_cv_trainval"]
    out["W_width_ladder"] = {
        "raw_relerr": lt["default@0.5"]["rel"],
        "quantile_relerr": tab["test"]["quantile_ratio_5bin"]["relerr"],
        "gaugehead_tiny_relerr": tab["test"]["extra_trees_logwidth_refit_trainval"]["relerr"],
        "theta_qcal_relerr": lt["theta*+qcal"]["rel"],
        "conformal_marginal_coverage": cc["coverage"],
        "conformal_worst_source_coverage": min(
            v["coverage"] for v in cc["per_source"].values()
        ),
    }

    prof = _load("krkcmd_profile_eval.json")["summary"]
    sig = _load("krkcmd_signal_width.json")
    multi = _load("krkcmd_multiinstance_eval.json")
    out["P_krkcmd_physical_um"] = {
        "author_dlm_test_mae_um": prof["DLMwidth(author)"]["group_split_test"]["MAE_um"],
        "gaugeprofile_cal_test_mae_um": prof["GaugeProfile-minrun5+linear-cal"]["group_split_test"]["MAE_um"],
        "signal_cnn_table_mae_um": sig["table_cnn_MAE"],
        "multiinstance_recall": multi["recall"],
        "multiinstance_mae_um": multi["MAE_um"],
        "multiinstance_median_um": multi["median_um"],
    }

    ub = _load("tless_upper_bound.json")["summary"]
    sam = _load("tless_sam3_eval.json")["prompts"]
    best = min(
        (p for p in sam.values() if p.get("match_rate", 0) > 0),
        key=lambda p: p["rel_err_median"],
    )
    out["D_tless_cad_mm"] = {
        "perfect_mask_rel_err_median": ub["rel_err_median"],
        "sam3_best_prompt_rel_err_median": best["rel_err_median"],
        "sam3_best_prompt_match_rate": best["match_rate"],
    }

    coins = _load("coins_mm_eval.json")["summary"]
    out["K_coins_known_object"] = {
        "loo_rel_err_mean": coins["rel_err_mean"],
        "pass_at_5pct": coins["pass@5pct"],
    }
    return out


def verify(actual: dict, expected: dict) -> list[str]:
    failures = []
    for track, spec in expected["tracks"].items():
        for metric, pin in spec["metrics"].items():
            got = actual.get(track, {}).get(metric)
            if got is None:
                failures.append(f"{track}.{metric}: MISSING")
                continue
            if abs(got - pin["value"]) > pin["tol"]:
                failures.append(
                    f"{track}.{metric}: got {got} expected {pin['value']} (tol {pin['tol']})"
                )
    return failures


TRACK_TITLES = {
    "S_crackseg9k_segmentation": "Track S — CrackSeg9k promptable crack segmentation (crack-only IoU, 3 seeds)",
    "W_width_ladder": "Track W — crack width calibration ladder (held-out sources, n=219, px GT)",
    "P_krkcmd_physical_um": "Track P — krkCMd physical crack width (manual μm GT)",
    "D_tless_cad_mm": "Track D — T-LESS industrial part dimensions (CAD mm GT)",
    "K_coins_known_object": "Track K — known-object scale consistency (real photos)",
}


def render(actual: dict, expected: dict) -> str:
    lines = [
        "# GaugeBench v1.0 — Leaderboard",
        "",
        f"Pinned {expected['pinned_date']} · regenerated by `python benchmark/collect_results.py`.",
        "Protocols, splits, and reproduction commands: [README.md](README.md).",
        "",
    ]
    for track, metrics in actual.items():
        lines.append(f"## {TRACK_TITLES[track]}")
        lines.append("")
        lines.append("| metric | value |")
        lines.append("|---|---:|")
        for k, v in metrics.items():
            lines.append(f"| {k} | {v:.4f} |")
        lines.append("")
    lines.append("외부 제출 환영 — 제출 절차는 README의 Submissions 절 참조.")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--check", action="store_true", help="검증만 수행 (리더보드 미갱신)")
    args = ap.parse_args()

    expected = json.loads(EXPECTED.read_text(encoding="utf-8"))
    actual = extract_metrics()
    failures = verify(actual, expected)
    if failures:
        print("GaugeBench drift detected:", file=sys.stderr)
        for f in failures:
            print(f"  {f}", file=sys.stderr)
        return 1
    print(f"GaugeBench v{expected['version']}: all pinned metrics verified "
          f"({sum(len(s['metrics']) for s in expected['tracks'].values())} checks).")
    if not args.check:
        LEADERBOARD.write_text(render(actual, expected), encoding="utf-8")
        print(f"wrote {LEADERBOARD.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
